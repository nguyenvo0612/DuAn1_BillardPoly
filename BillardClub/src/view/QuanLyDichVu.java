    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (checkInPut() == true) {
            String ma = txtMa.getText();
            String loaiDV = (String) cbxLoai.getSelectedItem();
            String ten = txtTen.getText();
            Double giaDouble = Double.valueOf(txtGia.getText());
            BigDecimal gia = BigDecimal.valueOf(giaDouble);
            int soLuong = Integer.parseInt(txtSoLuong.getText());
            int tinhTrang = 0;
            if (rdHD.isSelected()) {
                tinhTrang = 1;
            } else {
                tinhTrang = 0;
            }
            DichVu dv = new DichVu(ma, ten, giaDouble, loaiDV, soLuong, tinhTrang);
            if (service.create(dv) != 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
                return;
            }
            loadTable();
        }
    }//GEN-LAST:event_btnThemActionPerformed

