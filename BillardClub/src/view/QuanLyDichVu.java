public boolean checkInPut() {
        String ma = txtMa.getText(); // Chuỗi nhập vào

        // Biểu thức chính quy để kiểm tra chuỗi "NV" + số
        String regex = "^DV\\d+$";
        String regexGia = "\\d+";

        // Kiểm tra chuỗi đầu vào với biểu thức chính quy
        if (!Pattern.matches(regex, ma)) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên phải bắt đầu bằng DV + số");
            return false;
        }
        String ten = txtTen.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại tên dịch vụ");
            return false;
        }
        String gia = txtGia.getText();
        if (!Pattern.matches(regexGia, gia)) {
            JOptionPane.showMessageDialog(this, "Giá phải là số");
            return false;
        }
        String soLuong = txtSoLuong.getText();
        if (!Pattern.matches(regexGia, soLuong)) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số");
            return false;
        }
        return true;
    }

    public String doiTT(int tt) {
        if (tt == 0) {
            return "Không hoạt động";
        }
        return "Hoạt động";
    }
