    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        int haha = 0;
        ArrayList<String> listEmail = new ArrayList<>();

        for (KhachHang kh : listKH) {
            listEmail.add(kh.getEmail());

        }
        System.out.println(listEmail);
        for (int i = 0; i < listEmail.size(); i++) {

            final String username = "vontph27382@fpt.edu.vn";
            final String password = "Nguyenvo2003";
            //        String toEmail = txtTo.getText();
            String toEmail = listEmail.get(i);
            //            String[] toEmail = listEmail;

            String subJect = txtSubject.getText();
            String mess = txtMessage.getText();
            String toCC = listEmail.get(0);

            Properties prop = new Properties();
            prop.put("mail.smtp.auth", true);
            prop.put("mail.smtp.starttls.enable", "true");
            prop.put("mail.smtp.host", "smtp.gmail.com");
            prop.put("mail.smtp.port", "587");
            prop.put("mail.smtp.ssl.protocols", "TLSv1.2");
            prop.put("mail.smtp.ssl.trust", "smtp.gmail.com");

            Session session = Session.getInstance(prop,
                    new Authenticator() {
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });

            try {

                Message message = new MimeMessage(session);
                message.setFrom(new InternetAddress(username));
                for (String recipient : listEmail) {
                    message.addRecipient(Message.RecipientType.TO, new InternetAddress(recipient));
                }
                message.addRecipients(Message.RecipientType.CC,
                        InternetAddress.parse(toCC)
                );
                MimeBodyPart contentPart = new MimeBodyPart();
                contentPart.setContent(mess, "text/html; charset=utf-8");
                message.setSubject(subJect);
                message.setText(mess);

                MimeBodyPart filePart = new MimeBodyPart();

                File file = new File(link);

                FileDataSource fds = new FileDataSource(file);
                filePart.setDataHandler(new DataHandler(fds));
                filePart.setFileName(file.getName());

                MimeMultipart multipart = new MimeMultipart();

                multipart.addBodyPart(contentPart);
                multipart.addBodyPart(filePart);
                message.setContent(multipart);
                Transport.send(message);

                haha = 1;
            } catch (MessagingException e) {
                JOptionPane.showMessageDialog(this, "Unsent Email!");
            }

        }
        if (haha == 1) {
            JOptionPane.showMessageDialog(this, "Sent Email!");
            System.out.println("Done");
        }
    }//GEN-LAST:event_btnSendActionPerformed

