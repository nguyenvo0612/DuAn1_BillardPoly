public void loadTable() {
        model.setNumRows(0);
        ArrayList<CoSoVatChat> list = (ArrayList<CoSoVatChat>) coSoVatChatService.getAll();
        for (CoSoVatChat cs : list) {
            model.addRow(new Object[]{
                cs.getId(), cs.getMa(), cs.getTen(), cs.getTinhTrang()
            });
        }
    }
