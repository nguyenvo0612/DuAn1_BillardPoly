    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (validateInput() == true) {
            String ma = txtMaKM.getText();
            String ten = txtTenKM.getText();
            SimpleDateFormat dinhDang = new SimpleDateFormat("yyyy-MM-dd");
            String ngayBD = dinhDang.format(txtNgayBD.getDate());
            String ngayKT = dinhDang.format(txtNgayKT.getDate());
            Integer phanTram = Integer.valueOf(txtPhanTramGiam.getText());
            int tinhTrang = 0;
            if (rdoConHan.isSelected()) {
                tinhTrang = 1;
            } else {
                tinhTrang = 0;
            }

            KhuyenMai km = new KhuyenMai(ma, ten, phanTram, ngayBD, ngayKT, tinhTrang);
            if (khuyenMaiService.createKhuyenMaiRepository(km) == 1) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
            loadTable();
        }
    }//GEN-LAST:event_btnThemActionPerformed

