    private void tableKhuyenMaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKhuyenMaiMouseClicked
        int luaChon = tableKhuyenMai.getSelectedRow();
        if (luaChon > -1) {
            try {
                txtMaKM.setText(tableKhuyenMai.getValueAt(luaChon, 0).toString());
                txtTenKM.setText(tableKhuyenMai.getValueAt(luaChon, 1).toString());
                String bd = tableKhuyenMai.getValueAt(luaChon, 2).toString();
                java.util.Date ngayBD = new SimpleDateFormat("yyyy-MM-dd").parse(bd);
                txtNgayBD.setDate(ngayBD);
                java.util.Date ngayKT = new SimpleDateFormat("yyyy-MM-dd").parse(tableKhuyenMai.getValueAt(luaChon, 3).toString());
                txtNgayKT.setDate(ngayKT);
                txtPhanTramGiam.setText(tableKhuyenMai.getValueAt(luaChon, 4).toString());
                String tt = tableKhuyenMai.getValueAt(luaChon, 5).toString();
                Integer ttrang = (Integer) tableKhuyenMai.getValueAt(luaChon, 5);
                if (ttrang == 0) {
                    rdoHetHan.setSelected(true);
                } else {
                    rdoConHan.setSelected(true);

                }
            } catch (ParseException ex) {
                Logger.getLogger(QuanLyKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_tableKhuyenMaiMouseClicked
    public boolean validateInput() {
        String ma = txtMaKM.getText();
        if (ma.isBlank()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại mã");
            return false;
        }
        String ten = txtTenKM.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại tên chương trình");
            return false;
        }
        String regexso = "\\d+";

        String giam = txtPhanTramGiam.getText();
        if (!Pattern.matches(regexso, giam)) {
            JOptionPane.showMessageDialog(this, "% giảm phải là số");
            return false;
        }
        Date selectedDateBD = txtNgayBD.getDate();
        Date selectedDateKT = txtNgayKT.getDate();
        // kiểm tra tính hợp lệ của ngày
        if (selectedDateBD == null || selectedDateKT == null) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại ngày bắt đầu và kết thúc");
            return false;
        }
        if (selectedDateBD.after(selectedDateKT)) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải nhỏ hơn ngày kết thúc");
            return false;
        }
        return true;
    }

